#include <stdio.h>
#include <unistd.h>

int	ft_strlen(char *str)
{
	int	a;

	a = 0;
	while (str[a])
		a++;
	return (a);
}

int	main(void)
{
	char	*str;

	str = "abacaxi";
	printf("%d", ft_strlen(str));
	return (0);
}
