// Entendendo função

function fazerSuco(fruta1, fruta2) { //Parâmetros da função
    return `Suco de ${fruta1} e ${fruta2}` //Retorna o resultado da função
}

//A variável recebe o valor da função

let copo= fazerSuco("abacaxi", "ortelã") /*O chamado da função - Também conhecido como argumento*/

console.log(copo) //Mostra o resultado no console!