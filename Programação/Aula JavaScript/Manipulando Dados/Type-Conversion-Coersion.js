// Type coersion

console.log("5" + 5)

/*Nesse caso, no console, aparecerá um concatenação entre a string ("5") e o number (5). Automaticamente o JS aplicará o Type coersion, transformando o Number (5) numa String.*/

//Type conversion

console.log(Number("5") + 5)

/*Nesse caso, quem codificou, usou o Type conversion para fazer um conversão na String("5") para um Number(5) e assim houve a soma dos termos*/