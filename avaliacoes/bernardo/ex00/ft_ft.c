#include <unistd.h>

void	ft_ft(int *nbr)
{
	*nbr = 42;
}


int	main(void)
{
	int	n;
	int	d;
	int	u;

	ft_ft(&n);
	
	d = (n / 10) + '0';
	u = (n % 10) + '0';
	

	write(1, &d, 1);
	write(1, &u, 1);

	return(0);
}
