#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		write (1, str++, 1);
}

int main()
{
	char *str = "oilmklj";
	ft_putstr(str);
	return 0;
}

